<template>
  <div class="toast" v-show="isShow">
    <div>{{message}}</div>
  </div>
</template>

<script>
  export default {
    name: "Toast",
    data() {
      return{
        message: '',
        isShow: false
      }
    },
    methods:{
      show(msg,time = 2000) {
        this.message = msg
        this.isShow = true
        console.log('..........');
        setTimeout(()=>{
          this.message = ''
          this.isShow = false
        },time)
      }
    }
  }
</script>

<style scoped>
  .toast {
    position: fixed;
    top: 50%;
    left: 50%;
    transform: translate(-50%, -50%);
    padding: 5px 10px;
    background-color: rgba(0,0,0, 0.6);
    color: #ffff;
    border-radius: 8px;
    font-size: 20px;
    z-index: 999;
  }
</style>